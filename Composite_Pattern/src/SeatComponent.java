// Інтерфейс компонентів
// Він оголошує загальні методи для окремого місця і групи місць
interface SeatComponent {
    void reserve();   // Метод для бронювання місця або групи місць
    void release();   // Метод для вивільнення місця або групи місць
    boolean isReserved();  // Перевірка, чи заброньовано місце або всю групу місць
}